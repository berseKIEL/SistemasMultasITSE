    public void buscar() {
        String combobox1 = jComboBoxFiltrar.getSelectedItem().toString();
        if (combobox1 != "Ver Todos") {
            tablamulta = new TablaMultaModel(listamultas.buscar(jComboBoxFiltrar.getSelectedItem().toString()));
        } else {
            tablamulta = new TablaMultaModel(personaSeleccionada.getMultas());
        }
        jTableMultas.setModel(tablamulta);
        tablamulta.fireTableDataChanged();
    }

