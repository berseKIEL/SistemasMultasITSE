    private void enviardatos() {
        if (!(TextFieldApellido.getText().equals("") || TextFieldDNI.getText().equals("")
                || TextFieldNombre.getText().equals("") || TextFieldEmail.getText().equals(""))) {
            switch (bandera) {
                case 1:
                    if (listapers.buscarPersonaMDNI(Integer.parseInt(TextFieldDNI.getText())) == false) {
                        listapers.guardar(new Persona(Integer.parseInt(TextFieldDNI.getText()),
                                TextFieldNombre.getText(),
                                TextFieldApellido.getText(),
                                TextFieldEmail.getText()));

                        tablainfractor.fireTableDataChanged();
                        JOptionPane.showMessageDialog(this, "Alta exitosa!", "Alta de Infractor", JOptionPane.INFORMATION_MESSAGE);
                        inicioBotones();
                        break;
                    } else {
                        JOptionPane.showMessageDialog(this, "El Dni que ingresaste ya existe", "Error:", JOptionPane.ERROR_MESSAGE);
                        inicioBotones();
                        limpiarDataTextField();
                        jTable1.setEnabled(true);
                        desactivartextfield();
                        break;
                    }
                case 2:
                    if (listapers.buscarPersonaMDNI(Integer.parseInt(TextFieldDNI.getText())) == false 
                            || DNI==Integer.parseInt(TextFieldDNI.getText())  ) {
                        activartextfield();
                        listapers.modificarPorDNI(DNI,
                                Integer.parseInt(TextFieldDNI.getText()),
                                TextFieldNombre.getText(),
                                TextFieldApellido.getText(),
                                TextFieldEmail.getText());
                        tablainfractor.fireTableDataChanged();
                        JOptionPane.showMessageDialog(this, "Modificacion exitosa!", "Modificar Infractor", JOptionPane.INFORMATION_MESSAGE);
                        inicioBotones();
                        limpiarDataTextField();
                        jTable1.setEnabled(true);
                        desactivartextfield();
                        actualizardatos();
                        break;
                    } else {
                        JOptionPane.showMessageDialog(this, "El Dni que ingresaste ya existe", "Error:", JOptionPane.ERROR_MESSAGE);
                        TextFieldDNI.requestFocus();
                        TextFieldDNI.selectAll();
                        break;
                    }

            }

        } else {
            JOptionPane.showMessageDialog(this, "Faltan datos a ingresar", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
