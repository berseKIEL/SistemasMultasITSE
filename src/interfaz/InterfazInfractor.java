    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        if (evt.getClickCount() == 2) {
            Agregar.setEnabled(false);
            Eliminar.setEnabled(false);
            Modificar.setEnabled(false);
            Cancelar.setEnabled(true);
            Guardar.setEnabled(false);
            desactivartextfield();
            InterfazMultas interfazmultas = new InterfazMultas();
            interfazmultas.setVisible(true);
        }
        desactivartextfield();
        JTable datosfila = (JTable) evt.getSource();
        //COPIO LOS DATOS DE LA SELECCION EN LOS TEXT FIELDS
        TextFieldDNI.setText(datosfila.getModel().getValueAt(datosfila.getSelectedRow(), 0).toString());
        TextFieldNombre.setText(datosfila.getModel().getValueAt(datosfila.getSelectedRow(), 1).toString());
        TextFieldApellido.setText(datosfila.getModel().getValueAt(datosfila.getSelectedRow(), 2).toString());
        TextFieldEmail.setText(datosfila.getModel().getValueAt(datosfila.getSelectedRow(), 3).toString());

        Agregar.setEnabled(false);
        Eliminar.setEnabled(true);
        Modificar.setEnabled(true);
        VerMultas.setEnabled(true);
        Cancelar.setEnabled(false);


    }//GEN-LAST:event_jTable1MouseClicked

