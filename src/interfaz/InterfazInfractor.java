    public void buscar() {
        tablainfractor = new TablaInfractorModel(listapers.buscar(Integer.parseInt(JTextFieldBusqueda.getText())));
        if (tablainfractor.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Dni Inexistente", "Informacion:", JOptionPane.INFORMATION_MESSAGE);
        } else {
            jTable1.setModel(tablainfractor);
            tablainfractor.fireTableDataChanged();
        }

    }
