    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
        desactivartextfield();
        int accion = JOptionPane.showConfirmDialog(this, "¿Estás seguro que deseas eliminar?");
        if (accion == JOptionPane.YES_OPTION) {
            listapers.eliminarPorDNI(Integer.parseInt(TextFieldDNI.getText()));
            tablainfractor.fireTableDataChanged();
            JOptionPane.showMessageDialog(this, "Se elimino con exito", "Eliminacion", JOptionPane.INFORMATION_MESSAGE);
            inicioBotones();
            limpiarDataTextField();
            desactivartextfield();
            actualizardatos();
        } else if (accion == JOptionPane.NO_OPTION) {

        } else if (accion == JOptionPane.CANCEL_OPTION) {
            limpiarDataTextField();
            inicioBotones();
            limpiarDataTextField();
            desactivartextfield();
            jTable1.clearSelection();
            actualizardatos();
        }

    }//GEN-LAST:event_EliminarActionPerformed

